/*
for ASCII VALUES of characters simply do type casting
*/
package basichashing;

public class AsciiValuesOfCharacters 
{
    public static void main(String[] args)
    {
        char c ='c'; // character c
        
        int a=(int)c; // type casting to integer
        
        System.out.println("ASCII VALUE of c : "+a);
        
    }
}
